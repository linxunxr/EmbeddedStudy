#include <stdio.h>

// switch语句
int main()
{
    int num = 3;

    switch (num) {
    case 1:
        printf("1111111\n");
        break;
    case 2:
        printf("222222222\n");
        break;
    case 3:
        printf("333333333\n");
        break;
    default:
        printf("xxxxxxxx\n");
        break;
    }
    return 0;
}
